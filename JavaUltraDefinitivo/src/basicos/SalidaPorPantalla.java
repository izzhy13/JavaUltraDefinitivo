package basicos;
public class SalidaPorPantalla {
    public static void main(String[] args) {
        System.out.println("Hola mundo");         // Imprime y hace salto de línea
        System.out.print("Hola ");                // Imprime sin salto
        System.out.print("mundo");                // Se va a imprimir en la misma línea
        System.out.printf("Tienes %d años\n", 20); // Imprime con formato
    }
}

